int	ft_strcmp(char *s1, char *s2)
{
	int len;
	int i;

	len = 0;
	i = 0;
	while (s1[len] != '\0')
	{
		++len;
	}
	while (s2[i] != '\0')
	{
		++i;
	}
	return (len - i);
}
