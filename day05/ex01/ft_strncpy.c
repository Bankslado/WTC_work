char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int j;

	j = 0;
	while (j <= n)
	{
		dest[j] = src[j];
		++j;
	}
	dest[j + 1] = '\0';
	return (dest);
}
